package com.example.hospital;

public class ObjectSemuaPoli {

    private int noantrian;
    private String nama, poli, dokter, tanggal, jenis;

    public ObjectSemuaPoli(int noantrian, String nama, String poli, String dokter, String tanggal, String jenis) {
        this.noantrian = noantrian;
        this.nama = nama;
        this.poli = poli;
        this.dokter = dokter;
        this.tanggal = tanggal;
        this.jenis = jenis;

    }
}
